package com.matis;

public class PrimitivesTypes {
    // Numbers without decimal
    byte byteType = Byte.MAX_VALUE; // 1 byte
    short shortType = Short.MAX_VALUE; // 2 bytes
    int intType = Integer.MAX_VALUE; // 4 bytes
    long longType = Long.MAX_VALUE; // 8 bytes

    // Numbers with decimal
    float floatType = Float.MAX_VALUE; // 4 bytes
    double doubleType = Double.MAX_VALUE; // 8 bytes

    // Others
    char charType = 'B'; // single character like Cpp
    boolean booleanType = true; // true/false like Cpp/JS

    // Interesting

    long seperatedLong = 3_432_234_234L;
    float decimalNumber = 3.12F;
}
